function sumar(num1, num2) {
    const resultado = num1 + num2;
    console.log(`La suma de ${num1} más ${num2} es igual a ${resultado}`);
}

// Ejemplo de uso
sumar(5, 3);
function restar(num1, num2) {
    const resultado = num1 - num2;
    console.log(`La resta de ${num1} menos ${num2} es igual a ${resultado}`);
}

// Ejemplo de uso
restar(5, 3);
function multiplicar(num1, num2) {
    const resultado = num1 * num2;
    console.log(`La multiplicación de ${num1} por ${num2} es igual a ${resultado}`);
}

// Ejemplo de uso
multiplicar(5, 3);

function dividir(num1, num2) {
    const resultado = num1 / num2;
    console.log(`La división de ${num1} entre ${num2} es igual a ${resultado}`);
}

// Ejemplo de uso
dividir(5, 3);
